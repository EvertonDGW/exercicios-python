# Cria uma lista chamada lista_a contendo os elementos 1, 2 e 3.
lista_a = [1, 2, 3]

# Cria outra lista chamada lista_b contendo os elementos 4, 5 e 6.
lista_b = [4, 5, 6]

# Concatena as listas lista_a e lista_b e atribui o resultado à variável lista_c.
lista_c = lista_a + lista_b
#se voce digitar print(lista_c) vai exibir uma lista unica com os elementos de lista_a e lista_b

# Estende a lista lista_a adicionando os elementos da lista_b ao final dela.
lista_a.extend(lista_b)


# Imprime a lista lista_a após as operações realizadas.
print(lista_a)